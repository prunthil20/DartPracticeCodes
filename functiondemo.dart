void main() {
  var a = sum(10, 5);
  print(a);
  var b = sub(10, 5);
  print(b);
  var c = mul(10, 5);
  print(c);
  var d = div(10, 5);
  print(d);
}

sum(int a, int b) {
  var e = a + b;
  return e;
}

sub(int a, int b) {
  var f = a - b;
  return f;
}

mul(int a, int b) {
  var g = a * b;
  return g;
}

div(int a, int b) {
  var h = a / b;
  return h;
}
