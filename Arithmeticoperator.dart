void main() {
  var n1 = 10;
  var n2 = 5;
  var answer = 0;

  answer = n1 + n2;
  print("Addition : $answer");

  answer = n1 - n2;
  print("Subtraction : $answer");

  answer = n1 * n2;
  print("Multiplication : $answer");

  answer = n1 ~/ n2;
  print("Division : $answer");

  answer = n1 % n2;
  print("Reminder : $answer");
}
